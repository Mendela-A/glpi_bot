import pytest
from utils import strip_html


@pytest.mark.parametrize("raw, expected", [
    # Типовий GLPI-контент з параграфами
    ("<p>3123</p><p> </p>", "3123"),
    ("<p>Привіт</p><p>Світ</p>", "Привіт\n\nСвіт"),

    # br-теги
    ("Рядок 1<br>Рядок 2", "Рядок 1\nРядок 2"),
    ("Рядок 1<br/>Рядок 2", "Рядок 1\nРядок 2"),
    ("Рядок 1<BR />Рядок 2", "Рядок 1\nРядок 2"),

    # HTML-ентіті
    ("&lt;b&gt;текст&lt;/b&gt;", "<b>текст</b>"),
    ("AT&amp;T", "AT&T"),
    ("ціна &gt; 100", "ціна > 100"),

    # Вкладені теги
    ("<p><strong>Важливо</strong>: перевірте</p>", "Важливо: перевірте"),

    # Зайві порожні рядки (більше 2 підряд → 2)
    ("<p>A</p><p></p><p></p><p>B</p>", "A\n\nB"),

    # Порожній рядок / пробіли
    ("", ""),
    ("   ", ""),
    ("<p>  </p>", ""),

    # Чистий текст без тегів — не чіпаємо
    ("Звичайний текст", "Звичайний текст"),
])
def test_strip_html(raw, expected):
    assert strip_html(raw) == expected
